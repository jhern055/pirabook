            dataType: 'json',
            data: {
                idrecord: idrecord,
                qty: quantity_wanted,
                price:price,
            },
            beforeSend: function(response) {
            },
            success: function(response) {
                $("span#textDetailCart").html(response.textDetailCart);
                window.location.href="<?php echo base_url(); ?>cart";
            }
    });
});

</script>
